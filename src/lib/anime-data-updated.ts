export const getCharactersByBirthPlace = (place: string): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(char => char.origen.includes(place));
};

export const getCharactersByCrew = (crew: string): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(char => 
    char.crew.toLowerCase().includes(crew.toLowerCase())
  );
};

export const getCharactersByBountyRange = (min: number, max: number): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(char => {
    if (char.recompensa === null) return false;
    return char.recompensa >= min && char.recompensa <= max;
  });
};

export const getCharactersWithHaki = (hakiType?: string): AnimeCharacter[] => {
  if (!hakiType) return ANIME_CHARACTERS.filter(char => char.haki);
  
  return ANIME_CHARACTERS.filter(char => 
    char.tipo_haki.some(type => type.toLowerCase().includes(hakiType.toLowerCase()))
  );
};

export const getCharactersWithDevilFruit = (): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(char => char.fruta_diablo !== null);
};

export const getCharactersByRole = (role: 'main' | 'supporting'): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(char => char.role === role);
};

export const getRandomCharacter = (): AnimeCharacter => {
  const randomIndex = Math.floor(Math.random() * ANIME_CHARACTERS.length);
  return ANIME_CHARACTERS[randomIndex];
};

export const getCharacterByName = (name: string): AnimeCharacter | undefined => {
  return ANIME_CHARACTERS.find(char => 
    char.name.toLowerCase().includes(name.toLowerCase()) ||
    char.nameEnglish?.toLowerCase().includes(name.toLowerCase())
  );
};

export const getCharactersByAnime = (animeId: string): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(char => char.animeId === animeId);
};

export const getOnePieceCharacters = (): AnimeCharacter[] => {
  return getCharactersByAnime('onepiece');
};

// Helper functions for filtering by new characteristics
export const getCharactersByRecompensa = (minRecompensa?: number, maxRecompensa?: number): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(character => {
    if (character.recompensa === null) return false;
    if (minRecompensa && character.recompensa < minRecompensa) return false;
    if (maxRecompensa && character.recompensa > maxRecompensa) return false;
    return true;
  });
};

export const getCharactersByOrigen = (origen: string): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(character => 
    character.origen.toLowerCase().includes(origen.toLowerCase())
  );
};

export const getCharactersByTipoHaki = (tipoHaki: string): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(character => 
    character.tipo_haki.some(haki => 
      haki.toLowerCase().includes(tipoHaki.toLowerCase())
    )
  );
};

export const getCharactersByFrutaDiablo = (frutaName?: string): AnimeCharacter[] => {
  if (frutaName) {
    return ANIME_CHARACTERS.filter(character => 
      character.fruta_diablo && 
      character.fruta_diablo.toLowerCase().includes(frutaName.toLowerCase())
    );
  }
  // Return characters with any devil fruit
  return ANIME_CHARACTERS.filter(character => 
    character.fruta_diablo && character.fruta_diablo !== null
  );
};

export const getCharactersWithoutFrutaDiablo = (): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(character => 
    character.fruta_diablo === null
  );
};

export const getCharactersByHakiTypes = (hakiTypes: string[]): AnimeCharacter[] => {
  return ANIME_CHARACTERS.filter(character => 
    hakiTypes.every(haki => 
      character.tipo_haki.some(charHaki => 
        charHaki.toLowerCase().includes(haki.toLowerCase())
      )
    )
  );
};

export const getTopRecompensas = (limit: number = 10): AnimeCharacter[] => {
  return ANIME_CHARACTERS
    .filter(character => character.recompensa !== null)
    .sort((a, b) => (b.recompensa || 0) - (a.recompensa || 0))
    .slice(0, limit);
};

export const getCharacterStats = () => {
  const total = ANIME_CHARACTERS.length;
  const withRecompensa = ANIME_CHARACTERS.filter(c => c.recompensa !== null).length;
  const withFrutaDiablo = ANIME_CHARACTERS.filter(c => c.fruta_diablo !== null).length;
  const withHaki = ANIME_CHARACTERS.filter(c => c.tipo_haki.length > 0).length;
  const conquerorHaki = ANIME_CHARACTERS.filter(c => 
    c.tipo_haki.some(h => h.toLowerCase().includes('conquistador'))
  ).length;

  return {
    total,
    withRecompensa,
    withFrutaDiablo,
    withHaki,
    conquerorHaki,
    withoutRecompensa: total - withRecompensa,
    withoutFrutaDiablo: total - withFrutaDiablo,
    withoutHaki: total - withHaki
  };
};
